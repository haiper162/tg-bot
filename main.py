async def handle_callback(update: Update, context: ContextTypes.DEFAULT_TYPE):
    query = update.callback_query
    chat_id = query.message.chat.id
    data = query.data

    if chat_search_count.get(chat_id, 0) >= 2 and data != "new_search":
        try:
            await query.answer()
            await query.message.reply_text(translate("–©–æ–± –ø—Ä–æ–¥–æ–≤–∂–∏—Ç–∏, –∞–≤—Ç–æ—Ä–∏–∑—É–π—Ç–µ—Å—å –∞–±–æ –∑–∞—Ä–µ—î—Å—Ç—Ä—É–π—Ç–µ—Å—å:", user_lang.get(chat_id, 'uk')))
        except telegram.error.BadRequest:
            return
        except telegram.error.Forbidden:
            return
        return

    if data == "select_language":
        keyboard, row = [], []
        for i, (lang, code) in enumerate(LANGUAGES.items(), 1):
            row.append(InlineKeyboardButton(lang, callback_data=code))
            if i % 3 == 0:
                keyboard.append(row)
                row = []
        if row:
            keyboard.append(row)
        try:
            await query.answer()
            await query.message.reply_text("‚¨áÔ∏è Select a language from the list below:", reply_markup=InlineKeyboardMarkup(keyboard))
        except telegram.error.BadRequest:
            return
        except telegram.error.Forbidden:
            return
        return

    if data == "new_search":
        try:
            await query.answer()
        except telegram.error.BadRequest:
            return
        except telegram.error.Forbidden:
            return
        await half_sequence(update, context, user_lang.get(chat_id, 'uk'))
        chat_search_count[chat_id] = chat_search_count.get(chat_id, 0) + 1
        if chat_search_count[chat_id] >= 2:
            buttons = [
                [InlineKeyboardButton(translate("–ê–≤—Ç–æ—Ä–∏–∑—É–≤–∞—Ç–∏—Å—å", user_lang.get(chat_id, 'uk')), url=SMART_LINK)],
                [InlineKeyboardButton(translate("–ó–∞—Ä–µ—î—Å—Ç—Ä—É–≤–∞—Ç–∏—Å—å", user_lang.get(chat_id, 'uk')), url=SMART_LINK)],
            ]
            try:
                await query.message.reply_text(
                    translate("–©–æ–± –ø—Ä–æ–¥–æ–≤–∂–∏—Ç–∏, –∞–≤—Ç–æ—Ä–∏–∑—É–π—Ç–µ—Å—å –∞–±–æ –∑–∞—Ä–µ—î—Å—Ç—Ä—É–π—Ç–µ—Å—å:", user_lang.get(chat_id, 'uk')),
                    reply_markup=InlineKeyboardMarkup(buttons),
                )
            except telegram.error.Forbidden:
                return
        return

    user_lang[chat_id] = data
    lang_name = next((k for k, v in LANGUAGES.items() if v == data), "")
    try:
        await query.answer()
        await query.message.reply_text(f"‚úÖ {translate('–ú–æ–≤—É –≤—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–æ', data)}: {lang_name}")
    except telegram.error.BadRequest:
        return
    except telegram.error.Forbidden:
        return

    dummy_update = Update(update.update_id, message=query.message)
    await full_sequence(dummy_update, context, data)

app = ApplicationBuilder().token(TOKEN).build()
app.add_handler(CommandHandler("start", start))
app.add_handler(CallbackQueryHandler(handle_callback))
app.add_handler(MessageHandler(filters.TEXT & (~filters.COMMAND), handle_msg))
app.run_polling()
